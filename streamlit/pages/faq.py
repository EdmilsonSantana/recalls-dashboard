import streamlit as st

# O que é recall
st.title('FAQ')

st.subheader('O que é um recall?')
st.write('''**Recall** é um termo usado em diversos contextos, mas geralmente se refere a uma ação de correção ou retirada de um produto ou serviço que apresentou problemas de segurança ou eficácia.''')
st.write('''Um recall de veículos é uma ação corretiva realizada pela fabricante de um automóvel devido a problemas identificados com alguma peça ou componente do veículo.
 Esse tipo de recall é feito para corrigir questões de segurança ou de funcionamento do veículo antes que elas possam causar problemas para os proprietários ou para outros usuários da estrada.
 Em geral, o proprietário do veículo é notificado pela fabricante sobre o recall e pode levar o veículo para ser consertado em uma concessionária autorizada sem custo algum.''')

# O que é NHTSA
st.subheader('O que é NTHSA?')
st.markdown('''**NHTSA** é a sigla para *National Highway Traffic Safety Administration*, que em português significa Administração Nacional de Segurança de Tráfego Rodoviário.
 É uma agência do departamento de transporte dos Estados Unidos responsável por regulamentar e garantir a segurança de veículos e equipamentos relacionados à segurança viária,
 incluindo regulamentação de normas para veículos, equipamentos de segurança, e a investigação de acidentes de trânsito.
 A NHTSA também é responsável por monitorar e realizar recalls de veículos quando necessário, a fim de garantir a segurança dos usuários da estrada.''')
